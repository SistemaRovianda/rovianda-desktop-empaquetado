package com.rovianda.utility.ConnectService.Models;

public class Error {
    int code;
    String message;

    public int getCode() {
        return code;
    }

    public void setCode(int conde) {
        this.code = conde;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
